    private void loadTable(SHKI sh ,Profesori p){
        DefaultTableModel model = (DefaultTableModel) resultTable.getModel();
        ArrayList<Student> list = sh.getStudList();
        Object rowData[] = new Object[11];
        for(int i =0 ;i <list.size();i++){
            rowData[0]= list.get(i).getName();rowData[1]= list.get(i).getSurname();
            rowData[2]= list.get(i).getID();
            rowData[3]= list.get(i).getResults().getAssingments()+" %";
            rowData[4]=list.get(i).getResults().getGroup_project()+" %";
            rowData[5]=list.get(i).getResults().getMid_term1()+" %";
            rowData[6]=list.get(i).getResults().getMid_term2()+" %";
            rowData[7]=list.get(i).getResults().getFinal_test()+" %";
            rowData[8]=list.get(i).getResults().getAttendance()+" %";
            rowData[9]=list.get(i).getResults().getTotalPoints();
            rowData[10]=list.get(i).getResults().getGrade();
            model.addRow(rowData);
            
            
            
            
        }
    }
    
    
